fn part1()
{
    let lines = raw_input().trim().lines().map(|line| line.trim().chars());

    let mut sum = 0;
    'loop_lines: for line in lines {
        let mut chunks = Vec::new();

        for char in line {
            match char {
                '[' => chunks.push(char),
                '(' => chunks.push(char), 
                '{' => chunks.push(char),
                '<' => chunks.push(char),
                ']' => {
                    let last = chunks.pop().unwrap();
                    if last != '[' {
                        sum += 57;
                        continue 'loop_lines;
                    }
                },
                ')' => {
                    let last = chunks.pop().unwrap();
                    if last != '(' {
                        sum += 3;
                        continue 'loop_lines;
                    }
                },
                '}' => {
                    let last = chunks.pop().unwrap();
                    if last != '{' {
                        sum += 1197;
                        continue 'loop_lines;
                    }
                },
                '>' => {
                    let last = chunks.pop().unwrap();
                    if last != '<' {
                        sum += 25137;
                        continue 'loop_lines;
                    }
                },
                _ => panic!(),
            };
        }
    }

    println!("Part 1: {}", sum);
}

