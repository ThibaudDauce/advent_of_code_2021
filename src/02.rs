use std::str::FromStr;

fn main() {
    part1();
    part2();
}

#[derive(Debug)]
struct ParseCommandError {}

enum Command {
    Forward(u32),
    Up(u32),
    Down(u32),
}

impl FromStr for Command {
    type Err = ParseCommandError;

    fn from_str(s: &str) -> Result<Self, Self::Err> {
        let split = s.trim().split_once(' ');

        match split {
            None => Err(ParseCommandError {}),
            Some((command, value_as_string)) => {
                let maybe_value: Result<u32, _> = value_as_string.parse();
                match maybe_value {
                    Err(_) => Err(ParseCommandError {}),
                    Ok(value) => match command {
                        "forward" => Ok(Command::Forward(value)),
                        "up"      => Ok(Command::Up(value)),
                        "down"    => Ok(Command::Down(value)),
                        _         => Err(ParseCommandError {}),
                    }
                }
            }
        }
    }
}

struct PositionPart1 {
    horizontal: i32,
    depth: i32,
}

fn part1() {
    let commands: Vec<Command> = raw_input()
        .trim()
        .lines()
        .map(|line| line.parse().unwrap())
        .collect();

    let mut position = PositionPart1 { horizontal: 0, depth: 0 };

    for command in commands {
        match command {
            Command::Forward(value) => position.horizontal += value as i32,
            Command::Up(value)      => position.depth -= value as i32,
            Command::Down(value)    => position.depth += value as i32,
        }
    }

    println!("Part 1: {}", position.horizontal * position.depth);
}

struct PositionPart2 {
    horizontal: i32,
    aim: i32,
    depth: i32,
}

fn part2() {
    let commands: Vec<Command> = raw_input()
        .trim()
        .lines()
        .map(|line| line.parse().unwrap())
        .collect();

    let mut position = PositionPart2 { horizontal: 0, aim: 0, depth: 0 };

    for command in commands {
        match command {
            Command::Forward(value) => {
                position.horizontal += value as i32;
                position.depth += position.aim * (value as i32);
            },
            Command::Up(value)      => position.aim -= value as i32,
            Command::Down(value)    => position.aim += value as i32,
        }
    }

    println!("Part 2: {}", position.horizontal * position.depth);
}

